// 한글 획순 조합 엔진 — 자모의 획 데이터를 조합해 어떤 글자든 만들어요
// 좌표계: 0~100 정사각 셀 (0,0 = 왼쪽 위). 획은 폴리라인 {p:[[x,y]...]} 또는 원 {c:[cx,cy,r]}
const HangulData = (() => {
  const INITIALS = ['ㄱ','ㄲ','ㄴ','ㄷ','ㄸ','ㄹ','ㅁ','ㅂ','ㅃ','ㅅ','ㅆ','ㅇ','ㅈ','ㅉ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'];
  const MEDIALS = ['ㅏ','ㅐ','ㅑ','ㅒ','ㅓ','ㅔ','ㅕ','ㅖ','ㅗ','ㅘ','ㅙ','ㅚ','ㅛ','ㅜ','ㅝ','ㅞ','ㅟ','ㅠ','ㅡ','ㅢ','ㅣ'];
  const FINALS = ['','ㄱ','ㄲ','ㄳ','ㄴ','ㄵ','ㄶ','ㄷ','ㄹ','ㄺ','ㄻ','ㄼ','ㄽ','ㄾ','ㄿ','ㅀ','ㅁ','ㅂ','ㅄ','ㅅ','ㅆ','ㅇ','ㅈ','ㅊ','ㅋ','ㅌ','ㅍ','ㅎ'];

  // 자모별 획 데이터 (그리는 순서 = 배열 순서, 점 순서 = 방향)
  const J = {
    'ㄱ': [{p:[[15,20],[82,18],[64,86]]}],
    'ㄴ': [{p:[[28,12],[28,80],[85,82]]}],
    'ㄷ': [{p:[[18,18],[82,18]]},{p:[[22,22],[22,82],[82,82]]}],
    'ㄹ': [{p:[[24,16],[74,16],[56,42]]},{p:[[26,42],[74,42]]},{p:[[26,42],[26,86],[78,86]]}],
    'ㅁ': [{p:[[24,18],[24,82]]},{p:[[24,18],[80,18],[80,82]]},{p:[[24,82],[80,82]]}],
    'ㅂ': [{p:[[26,14],[26,86]]},{p:[[72,34],[72,86]]},{p:[[26,52],[72,52]]},{p:[[26,86],[72,86]]}],
    'ㅅ': [{p:[[50,16],[20,84]]},{p:[[52,40],[82,84]]}],
    'ㅇ': [{c:[50,50,33]}],
    'ㅈ': [{p:[[22,24],[78,24]]},{p:[[50,24],[24,82]]},{p:[[52,42],[80,82]]}],
    'ㅊ': [{p:[[40,10],[60,10]]},{p:[[22,32],[78,32]]},{p:[[50,32],[26,84]]},{p:[[52,48],[80,84]]}],
    'ㅋ': [{p:[[16,20],[82,18],[66,84]]},{p:[[22,50],[74,50]]}],
    'ㅌ': [{p:[[20,18],[80,18]]},{p:[[24,50],[78,50]]},{p:[[22,20],[22,84],[80,84]]}],
    'ㅍ': [{p:[[16,24],[84,24]]},{p:[[32,26],[30,80]]},{p:[[68,26],[70,80]]},{p:[[16,82],[84,82]]}],
    'ㅎ': [{p:[[38,8],[62,8]]},{p:[[22,30],[78,30]]},{c:[50,64,23]}],
    // 오른쪽(세로) 모음
    'ㅏ': [{p:[[46,10],[46,90]]},{p:[[46,50],[84,50]]}],
    'ㅑ': [{p:[[42,10],[42,90]]},{p:[[42,36],[80,36]]},{p:[[42,64],[80,64]]}],
    'ㅓ': [{p:[[18,50],[54,50]]},{p:[[54,10],[54,90]]}],
    'ㅕ': [{p:[[18,36],[54,36]]},{p:[[18,64],[54,64]]},{p:[[54,10],[54,90]]}],
    'ㅣ': [{p:[[50,10],[50,90]]}],
    'ㅐ': [{p:[[36,10],[36,90]]},{p:[[36,50],[64,50]]},{p:[[76,10],[76,90]]}],
    'ㅔ': [{p:[[22,50],[50,50]]},{p:[[50,10],[50,90]]},{p:[[76,10],[76,90]]}],
    'ㅒ': [{p:[[32,10],[32,90]]},{p:[[32,36],[62,36]]},{p:[[32,64],[62,64]]},{p:[[76,10],[76,90]]}],
    'ㅖ': [{p:[[20,36],[48,36]]},{p:[[20,64],[48,64]]},{p:[[48,10],[48,90]]},{p:[[76,10],[76,90]]}],
    // 아래(가로) 모음
    'ㅗ': [{p:[[50,14],[50,54]]},{p:[[12,54],[88,54]]}],
    'ㅛ': [{p:[[36,14],[36,54]]},{p:[[64,14],[64,54]]},{p:[[12,54],[88,54]]}],
    'ㅜ': [{p:[[12,46],[88,46]]},{p:[[50,46],[50,88]]}],
    'ㅠ': [{p:[[12,46],[88,46]]},{p:[[36,46],[36,86]]},{p:[[64,46],[64,86]]}],
    'ㅡ': [{p:[[12,50],[88,50]]}],
  };

  const DOUBLE = { 'ㄲ':'ㄱ','ㄸ':'ㄷ','ㅃ':'ㅂ','ㅆ':'ㅅ','ㅉ':'ㅈ' };
  const CLUSTER = {
    'ㄳ':['ㄱ','ㅅ'],'ㄵ':['ㄴ','ㅈ'],'ㄶ':['ㄴ','ㅎ'],'ㄺ':['ㄹ','ㄱ'],'ㄻ':['ㄹ','ㅁ'],
    'ㄼ':['ㄹ','ㅂ'],'ㄽ':['ㄹ','ㅅ'],'ㄾ':['ㄹ','ㅌ'],'ㄿ':['ㄹ','ㅍ'],'ㅀ':['ㄹ','ㅎ'],'ㅄ':['ㅂ','ㅅ'],
  };
  const V_COMPOUND = {
    'ㅘ':['ㅗ','ㅏ'],'ㅙ':['ㅗ','ㅐ'],'ㅚ':['ㅗ','ㅣ'],'ㅝ':['ㅜ','ㅓ'],
    'ㅞ':['ㅜ','ㅔ'],'ㅟ':['ㅜ','ㅣ'],'ㅢ':['ㅡ','ㅣ'],
  };
  const V_RIGHT = new Set(['ㅏ','ㅐ','ㅑ','ㅒ','ㅓ','ㅔ','ㅕ','ㅖ','ㅣ']);
  const V_BOTTOM = new Set(['ㅗ','ㅛ','ㅜ','ㅠ','ㅡ']);

  function decompose(ch) {
    const code = ch.charCodeAt(0) - 0xAC00;
    if (code < 0 || code > 11171) return null;
    const f = code % 28;
    const v = ((code - f) / 28) % 21;
    const i = ((code - f) / 28 - v) / 21;
    return { i: INITIALS[i], v: MEDIALS[v], f: FINALS[f] };
  }

  function mapInto(pt, b) {
    return [b.x0 + (pt[0] / 100) * (b.x1 - b.x0), b.y0 + (pt[1] / 100) * (b.y1 - b.y0)];
  }
  function place(strokes, b) {
    const sx = (b.x1 - b.x0) / 100, sy = (b.y1 - b.y0) / 100;
    return strokes.map((s) => s.c
      ? { c: [b.x0 + (s.c[0] / 100) * (b.x1 - b.x0), b.y0 + (s.c[1] / 100) * (b.y1 - b.y0), s.c[2] * Math.min(sx, sy)] }
      : { p: s.p.map((pt) => mapInto(pt, b)) });
  }

  // 자음(겹자음 포함)을 상자에 배치
  function consonant(j, b) {
    if (DOUBLE[j]) {
      const base = J[DOUBLE[j]];
      const w = b.x1 - b.x0;
      const left = { x0: b.x0, y0: b.y0, x1: b.x0 + w * 0.46, y1: b.y1 };
      const right = { x0: b.x0 + w * 0.54, y0: b.y0, x1: b.x1, y1: b.y1 };
      return [...place(base, left), ...place(base, right)];
    }
    return place(J[j] || [], b);
  }
  function finalConsonant(f, b) {
    if (CLUSTER[f]) {
      const [a, c] = CLUSTER[f];
      const w = b.x1 - b.x0;
      const left = { x0: b.x0, y0: b.y0, x1: b.x0 + w * 0.46, y1: b.y1 };
      const right = { x0: b.x0 + w * 0.54, y0: b.y0, x1: b.x1, y1: b.y1 };
      return [...consonant(a, left), ...consonant(c, right)];
    }
    return consonant(f, b);
  }

  function layout(v, hasFinal) {
    if (V_RIGHT.has(v)) {
      return hasFinal
        ? { I: { x0: 8, y0: 6, x1: 48, y1: 54 }, V: { x0: 50, y0: 4, x1: 90, y1: 58 }, F: { x0: 12, y0: 62, x1: 88, y1: 94 } }
        : { I: { x0: 8, y0: 12, x1: 50, y1: 88 }, V: { x0: 52, y0: 6, x1: 92, y1: 94 } };
    }
    if (V_BOTTOM.has(v)) {
      return hasFinal
        ? { I: { x0: 24, y0: 4, x1: 76, y1: 36 }, V: { x0: 8, y0: 38, x1: 92, y1: 64 }, F: { x0: 24, y0: 68, x1: 76, y1: 96 } }
        : { I: { x0: 22, y0: 8, x1: 78, y1: 48 }, V: { x0: 6, y0: 50, x1: 94, y1: 90 } };
    }
    // 복합 모음 (ㅘ, ㅝ, ㅚ …)
    return hasFinal
      ? { I: { x0: 8, y0: 6, x1: 42, y1: 40 }, Vb: { x0: 6, y0: 44, x1: 64, y1: 74 }, Vr: { x0: 58, y0: 4, x1: 90, y1: 78 }, F: { x0: 12, y0: 80, x1: 88, y1: 98 } }
      : { I: { x0: 8, y0: 8, x1: 46, y1: 46 }, Vb: { x0: 6, y0: 50, x1: 66, y1: 90 }, Vr: { x0: 60, y0: 6, x1: 92, y1: 90 } };
  }

  // 글자 하나의 모든 획을 (0~100 셀 좌표로) 반환
  function getSyllableStrokes(ch) {
    const d = decompose(ch);
    if (!d) return null;
    const hasFinal = d.f !== '';
    const L = layout(d.v, hasFinal);
    const out = [];
    out.push(...consonant(d.i, L.I));
    if (V_COMPOUND[d.v]) {
      const [bottom, right] = V_COMPOUND[d.v];
      out.push(...place(J[bottom], L.Vb));
      out.push(...place(J[right], L.Vr));
    } else {
      out.push(...place(J[d.v], L.V));
    }
    if (hasFinal) out.push(...finalConsonant(d.f, L.F));
    return out;
  }

  function isHangulSyllable(ch) {
    const c = ch.charCodeAt(0) - 0xAC00;
    return c >= 0 && c <= 11171;
  }

  // 자모 소리 (TTS로 읽어줄 글자: ㄹ→르, ㅏ→아)
  const CONS_SOUND = { 'ㄱ': '그', 'ㄲ': '끄', 'ㄴ': '느', 'ㄷ': '드', 'ㄸ': '뜨', 'ㄹ': '르', 'ㅁ': '므', 'ㅂ': '브', 'ㅃ': '쁘', 'ㅅ': '스', 'ㅆ': '쓰', 'ㅇ': '으', 'ㅈ': '즈', 'ㅉ': '쯔', 'ㅊ': '츠', 'ㅋ': '크', 'ㅌ': '트', 'ㅍ': '프', 'ㅎ': '흐' };
  const VOWEL_SOUND = { 'ㅏ': '아', 'ㅑ': '야', 'ㅓ': '어', 'ㅕ': '여', 'ㅗ': '오', 'ㅛ': '요', 'ㅜ': '우', 'ㅠ': '유', 'ㅡ': '으', 'ㅣ': '이', 'ㅐ': '애', 'ㅒ': '얘', 'ㅔ': '에', 'ㅖ': '예', 'ㅘ': '와', 'ㅙ': '왜', 'ㅚ': '외', 'ㅝ': '워', 'ㅞ': '웨', 'ㅟ': '위', 'ㅢ': '의' };
  function soundOf(j) { return CONS_SOUND[j] || VOWEL_SOUND[j] || j; }

  // 글자를 초성/중성/종성 슬롯으로 (셀 0~100 좌표의 중심 위치 포함)
  function boxCenter(b) { return [(b.x0 + b.x1) / 2, (b.y0 + b.y1) / 2]; }
  function jamoSlots(ch) {
    const d = decompose(ch); if (!d) return null;
    const hasF = d.f !== '';
    const L = layout(d.v, hasF);
    const slots = [{ role: 'i', jamo: d.i, c: boxCenter(L.I) }];
    if (V_COMPOUND[d.v]) {
      const a = boxCenter(L.Vb), b = boxCenter(L.Vr);
      slots.push({ role: 'v', jamo: d.v, c: [(a[0] + b[0]) / 2, (a[1] + b[1]) / 2] });
    } else slots.push({ role: 'v', jamo: d.v, c: boxCenter(L.V) });
    if (hasF) slots.push({ role: 'f', jamo: d.f, c: boxCenter(L.F) });
    return slots;
  }

  return { decompose, getSyllableStrokes, isHangulSyllable, soundOf, jamoSlots, INITIALS, MEDIALS };
})();
